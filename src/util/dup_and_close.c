#include <unistd.h>

int	dup_and_close(int fd1, int fd2)
{
	if (dup2(fd1, fd2) == -1)
	{
		close(fd1);
		return (-1);
	}
	if (close(fd1) == -1)
		return (-1);
	return (0);
}
